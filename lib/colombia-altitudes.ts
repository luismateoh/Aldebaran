// Función para obtener la altitud de un municipio
export function getAltitude(municipality: string, department: string): number | null {
  const departmentData = COLOMBIA_ALTITUDES[department]
  if (!departmentData) return null
  
  return departmentData[municipality] || null
}

// Función para buscar altitud con coincidencias parciales
export function findAltitude(municipalityQuery: string, departmentQuery: string): number | null {
  const normalizedMunicipality = municipalityQuery.toLowerCase().trim()
  const normalizedDepartment = departmentQuery.toLowerCase().trim()
  
  // Buscar departamento que contenga la consulta
  const departmentKey = Object.keys(COLOMBIA_ALTITUDES).find(dept => 
    dept.toLowerCase().includes(normalizedDepartment)
  )
  
  if (!departmentKey) return null
  
  const departmentData = COLOMBIA_ALTITUDES[departmentKey]
  
  // Buscar municipio que contenga la consulta
  const municipalityKey = Object.keys(departmentData).find(mun => 
    mun.toLowerCase().includes(normalizedMunicipality)
  )
  
  if (!municipalityKey) return null
  
  return departmentData[municipalityKey]
}